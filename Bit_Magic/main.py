a = 3
b = 6
#Bitwise AND
print(a & b)

#Bitwise OR
print(a | b)

#Bitwise XOR
print(a ^ b)

x = 5

#Left Shift
print(x << 1)
print(x << 2)
print(x << 3)

#Right Shift
print(x >> 1)
print(x >> 2)
print(x >> 3)
print(x >> 4)

#Bitwise Not
print(~x)

